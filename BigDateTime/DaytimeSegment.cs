namespace ExtendedNumerics;

/// <summary>
/// Specifies segments of the day.
/// </summary>
public enum DaytimeSegment {
    /// <summary>
    /// The morning segment (midnight to noon).
    /// </summary>
    AM = 0,
    /// <summary>
    /// The afternoon segment (noon to midnight).
    /// </summary>
    PM = 1,
}